       person’s thinking, perceptions, mood or behaviour. Mental illness can make it difficult for someone to 
       cope with work, relationships and other demands. The relationship between stress and mental illness is 
       complex, but it is known that stress can worsen an episode of mental illness. Most people can manage 
       their mental illness with medication, counselling or both.This page lists some of the more common mental
        health issues and mental illnesses.</p> 
